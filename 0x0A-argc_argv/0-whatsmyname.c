#include <stdio.h>
/**
 * main - function that returns arguments
 * @argc: number of parameters passed to the main function
 * @argv: array of pointers
 * Return: an integer
 */
int main(int __attribute__ ((unused)) argc, char *argv[])
{
	int i = 0;

	if (i == 0)
	{
		printf("%s\n", argv[i]);
	}
	return (0);
}
